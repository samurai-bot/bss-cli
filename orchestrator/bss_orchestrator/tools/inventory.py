"""Inventory read tools ‚Äî MSISDN pool + eSIM pool (hosted on CRM).

Reservation / assignment are internal SOM operations and are NOT exposed as
LLM tools ‚Äî the LLM only reads availability.
"""

from __future__ import annotations

from typing import Any

from ..clients import get_clients
from ..types import Iccid, Msisdn
from ._registry import register


@register("inventory.msisdn.list_available")
async def inventory_msisdn_list_available(
    prefix: str | None = None,
    limit: int = 20,
    status: str | None = "available",
) -> list[dict[str, Any]]:
    """List MSISDNs from the inventory pool.

    Default behaviour is the order-creation use case: status filter
    pinned to ``"available"``, no prefix, first ``limit`` rows. The
    operator cockpit also uses this tool for general pool inspection,
    so the parameters are widened:

    - ``prefix`` is OPTIONAL. Omit it to return the first ``limit``
      MSISDNs regardless of leading digits. Pass a 1-12 digit string
      to filter via SQL ``startswith`` ‚Äî ``prefix="8888"`` matches
      ``88880000``..``88889999``; ``prefix="8"`` matches every number
      whose MSISDN begins with 8.
    - ``status`` accepts ``"available"`` (default), ``"reserved"``,
      ``"assigned"``, ``"ported_out"``, or ``None`` for any state.
      Use ``status=None`` when the operator says "show me all
      numbers" ‚Äî they want the full pool, not just the sellable
      slice.

    Args:
        prefix: Optional digit prefix, e.g. ``"9000"`` or ``"8"``.
        limit: Maximum rows (default 20).
        status: One of ``available``, ``reserved``, ``assigned``,
            ``ported_out``, or ``None`` to disable the status filter.

    Returns:
        List of MSISDN dicts
        ``{msisdn, status, reserved_at?, assigned_to_subscription_id?}``.

    Raises:
        (none expected ‚Äî read tool)
    """
    return await get_clients().inventory.list_msisdns(
        state=status, prefix=prefix, limit=limit
    )


@register("inventory.msisdn.get")
async def inventory_msisdn_get(msisdn: Msisdn) -> dict[str, Any]:
    """Get status of a specific MSISDN.

    Args:
        msisdn: 8-digit mobile number.

    Returns:
        MSISDN dict ``{msisdn, state, assignedTo?, reservedUntil?}``.

    Raises:
        NotFound: MSISDN is not in the pool.
    """
    return await get_clients().inventory.get_msisdn(msisdn)


