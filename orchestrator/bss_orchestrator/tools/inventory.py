@register("inventory.msisdn.count")
async def inventory_msisdn_count(
    prefix: str | None = None,
) -> dict[str, Any]:
    """Group-by-status pool count. Source of truth for "how many
    numbers do we have?" / "is that all?" follow-ups.

    The list tool truncates at ``limit`` (default 20) and does not
    expose a total. Call this whenever the operator asks about pool
    size, depth, or whether a list result is partial ‚Äî never guess
    or infer from prior list output.

    ``prefix`` narrows the count to MSISDNs whose digits start with
    the given string (same ``startswith`` semantics as
    ``inventory.msisdn.list_available``); omit it for the whole pool.

    Args:
        prefix: Optional 1-12 digit prefix.

    Returns:
        ``{available, reserved, assigned, ported_out, total, prefix}``.
        Canonical state keys are always present (0 if no rows).

    Raises:
        (none expected ‚Äî read tool)
    """
    return await get_clients().inventory.count_msisdns(prefix=prefix)


@register("inventory.msisdn.add_range")
async def inventory_msisdn_add_range(
    prefix: str, count: int
) -> dict[str, Any]:
    """v0.17 ‚Äî bulk-extend the MSISDN pool by ``count`` contiguous numbers.

    The tool materializes the range as ``{prefix}{i:04d}`` for
    ``i in range(count)``. ``prefix`` MUST be 4-7 digits; the suffix
    is always 4 zero-padded digits, generated by the tool.

    Translating an operator's intent ‚Äî IMPORTANT:

    - "add 50 numbers from 88880000" / "starting at 88880000"
        ‚Üí prefix="8888", count=50 (NOT prefix="88880000"). The result
          is 88880000..88880049. Passing the full starting MSISDN as
          prefix is rejected by the policy (8 digits > the 7-digit
          maximum) and would otherwise produce 12-digit MSISDNs.
    - "add 1000 numbers under 9100"
        ‚Üí prefix="9100", count=1000 (range 91000000..91000999).
    - "add the 88889999 number"
        ‚Üí reject this shape: the tool only adds *contiguous ranges*.
          A single specific MSISDN is added by adding the prefix range
          that contains it (e.g. prefix="8888", count=10000).

    Operator-only: registered in the ``operator_cockpit`` profile, NOT
    in ``customer_self_serve``. Idempotent on overlap ‚Äî overlapping
    ranges re-run cleanly; existing rows (including ``ported_out``)
    are preserved.

    Args:
        prefix: 4-7 digit numeric prefix, e.g. ``"9100"`` or ``"8888"``.
        count:  Numbers to add (1..10000).

    Returns:
        ``{prefix, count, inserted, skipped, first, last}``.

    Raises:
        PolicyViolation: ``msisdn.add_range.sane_prefix`` ‚Äî
            non-digit prefix, prefix length outside [4, 7], or count
            outside [1, 10000].
    """
    return await get_clients().inventory.add_msisdn_range(prefix, count)


@register("inventory.esim.get_activation")
async def inventory_esim_get_activation(iccid: Iccid) -> dict[str, Any]:
    """Return the LPA activation-code record for an eSIM. Callers typically
    want this as ``{iccid, imsi, activationCode, msisdn}`` for QR rendering.
    For a subscription, prefer ``subscription.get_esim_activation`` which
    handles ICCID lookup.

    Args:
        iccid: eSIM ICCID (19-20 digits starting with ``8910101``).

    Returns:
        Activation dict.

    Raises:
        NotFound: unknown ICCID.
    """
    return await get_clients().inventory.get_activation_code(iccid)
